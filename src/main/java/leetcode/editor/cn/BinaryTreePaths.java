package leetcode.editor.cn;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;
import java.util.Queue;

import leetcode.editor.cn.struct.TreeNode;

public class BinaryTreePaths {
    public static void main(String[] args) {
        Solution solution = new BinaryTreePaths().new Solution();
    }
    //leetcode submit region begin(Prohibit modification and deletion)

    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {

        public List<String> binaryTreePaths(TreeNode root) {
            List<String> paths = new ArrayList<String>();
            constructPaths(root, new ArrayList<>(), paths);
            return paths;
        }

        public void constructPaths(TreeNode root, List<String> path, List<String> paths) {
            if (root != null) {
                List<String> pathSB = new ArrayList<>(path);
                pathSB.add(String.valueOf(root.val));
                if (root.left == null && root.right == null) {  // å½“å‰èŠ‚ç‚¹æ˜¯å¶å­èŠ‚ç‚¹
                    paths.add(String.join("->",pathSB));  // æŠŠè·¯å¾„åŠ å…¥åˆ°ç­”æ¡ˆä¸­
                } else {
                    constructPaths(root.left, pathSB, paths);
                    constructPaths(root.right, pathSB, paths);
                }
            }
        }


    }
    //leetcode submit region end(Prohibit modification and deletion)

    public List<String> binaryTreePaths(TreeNode root) {
        List<String> paths = new ArrayList<String>();
        if (root == null) {
            return paths;
        }
        Queue<TreeNode> nodeQueue = new LinkedList<TreeNode>();
        Queue<String> pathQueue = new LinkedList<String>();

        nodeQueue.offer(root);
        pathQueue.offer(Integer.toString(root.val));

        while (!nodeQueue.isEmpty()) {
            TreeNode node = nodeQueue.poll();
            String path = pathQueue.poll();

            if (node.left == null && node.right == null) {
                paths.add(path);
            } else {
                if (node.left != null) {
                    nodeQueue.offer(node.left);
                    pathQueue.offer(new StringBuffer(path).append("->").append(node.left.val).toString());
                }

                if (node.right != null) {
                    nodeQueue.offer(node.right);
                    pathQueue.offer(new StringBuffer(path).append("->").append(node.right.val).toString());
                }
            }
        }
        return paths;
    }

} 
